#include<stdio.h>
int main()
{
    int a[101][101],n,i,j,k;
    while(~scanf("%d",&n))
    {
        for(i=0;i<n;i++)
            for(j=0;j<n;j++)
            scanf("%d",&a[i][j]);
        k=0;
        if(a[0][0]+50<=a[0][1]&&a[0][0]+50<=a[1][0]) k++;
        if(a[0][n-1]+50<=a[0][n-2]&&a[0][n-1]+50<=a[1][n-1]) k++;
        for(i=1;i<n-1;i++)
        {
            if(a[0][i]+50<=a[0][i-1]&&a[0][i]+50<=a[0][i+1]&&a[0][i]+50<=a[1][i]) k++;
        }
        for(i=1;i<n-1;i++)
        {
            if(a[n-1][i]+50<=a[n-1][i-1]&&a[n-1][i]+50<=a[n-1][i+1]&&a[n-1][i]+50<=a[n-2][i]) k++;
        }
        for(i=1;i<n-1;i++)
        {
            if(a[i][0]+50<=a[i-1][0]&&a[i][0]+50<=a[i+1][0]&&a[i][0]+50<=a[i][1]) k++;
        }
         for(i=1;i<n-1;i++)
        {
            if(a[i][n-1]+50<=a[i-1][n-1]&&a[i][n-1]+50<=a[i+1][n-1]&&a[i][n-1]+50<=a[i][n-2]) k++;
        }
        for(i=1;i<n-1;i++)
            for(j=1;j<n-1;j++)
        {
            if(a[i][j]+50<=a[i-1][j]&&a[i][j]+50<=a[i+1][j]&&a[i][j]+50<=a[i][j+1]&&a[i][j]+50<=a[i][j-1]) k++;
        }
        printf("%d\n",k);
    }
    return 0;
}

