#include<stdio.h>
int main()
{
    double r,t,s,d;
    while (~scanf("%lf",&s))
        {
    if(s<=3500){r=0;d=0;}
    if(s>3500&&s<=5000){r=0.03;d=0;}
    if(s>5000&&s<=8000){r=0.1;d=105;}
    if(s>8000&&s<=12500){r=0.2;d=555;}
    if(s>12500&&s<=38500){r=0.25;d=1005;}
    if(s>38500&&s<=58500){r=0.3;d=2755;}
    if(s>58500&&s<=83500){r=0.35;d=5505;}
    if(s>83500){r=0.45;d=13505;}
    t=r*(s-3500)-d;
    printf("%.2f\n",t);
        }
    return 0;
}
