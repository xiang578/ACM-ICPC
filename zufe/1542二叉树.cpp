#include<stdio.h>
#include<string.h>
#include<stdlib.h>
#include<math.h>
int main()
{
   int ans,n,t;
   scanf("%d",&t);
    while(t--)
    {
        scanf("%d",&n);
        ans=0;
        if(n==0) break;
        while(n>0)
        {
            n=n/2;
            ans++;
        }
        printf("%d\n",ans-1);
    }
    return 0;
}
