#include "stdio.h"
#include"string.h"
int main()
{
   int i,len;
   char s[1000];
   while(~scanf("%s",s))
   {
    len=strlen(s);
    if(len==0) printf("Yes\n");
    else
    {
        for (i=0;i<len;i++)
        {
            if(s[i]!=s[len-i-1]) break;
        }
        if(i==len) printf("Yes\n");
        else printf("No\n");
    }
   }
    return 0;
}
